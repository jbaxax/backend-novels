// [MENTOR]: La importancia del evento dentro de la narrativa.
// Permite filtrar o resaltar eventos críticos cuando el autor revisa la línea de tiempo.
// 'critical' sería algo como "el protagonista descubre su origen" — un punto de no retorno.
export enum EventImportance {
  LOW = 'low',
  MEDIUM = 'medium',
  HIGH = 'high',
  CRITICAL = 'critical',
}
