// [MENTOR]: La dinámica emocional es el "cómo se siente" uno respecto al otro.
// Es distinto al tipo de relación: dos personajes pueden ser familia (tipo)
// pero sentir odio (dinámica). Esa tensión es lo que hace interesante la narrativa.
export enum EmotionalDynamic {
  HATE = 'hate',
  REPULSION = 'repulsion',
  ADMIRATION = 'admiration',
  LOVE = 'love',
  FEAR = 'fear',
  DISTRUST = 'distrust',
  INDIFFERENCE = 'indifference',
}
